/** Look up exercise by ID */
export function getExercise(id: string): Exercise | undefined {
  return EXERCISE_DATABASE.find(e => e.id === id);
}

/** Get exercises suitable for a given equipment set */
export function getExercisesForEquipment(availableEquipment: Equipment[]): Exercise[] {
  const equipSet = new Set(availableEquipment);
  return EXERCISE_DATABASE.filter(ex =>
    ex.equipment_required.every(eq => eq === "none" || equipSet.has(eq))
  );
}

/** Get exercises excluding certain risk tags (for injury filtering) */
export function getExercisesExcludingRisks(
  exercises: Exercise[],
  excludeRisks: RiskTag[]
): Exercise[] {
  const riskSet = new Set(excludeRisks);
  return exercises.filter(ex =>
    !ex.risk_tags.some(r => riskSet.has(r))
  );
}

/** Get exercises by modality */
export function getExercisesByModality(modality: Modality): Exercise[] {
  return EXERCISE_DATABASE.filter(e => e.modality === modality);
}

/** Get exercises by target muscle */
export function getExercisesForMuscle(muscle: MuscleGroup): Exercise[] {
  return EXERCISE_DATABASE.filter(e =>
    e.primary_muscles.includes(muscle) || e.secondary_muscles.includes(muscle)
  );
}
