/**
 * Get today's message based on the calendar date.
 * Same message all day, different one each day.
 */
export function getDailyMessage(seed?: number): string {
  const today = new Date();
  // Use year + day-of-year as the seed so it changes daily
  const start = new Date(today.getFullYear(), 0, 0);
  const diff = today.getTime() - start.getTime();
  const dayOfYear = Math.floor(diff / 86400000);
  const index = (seed !== undefined ? seed : (today.getFullYear() * 1000 + dayOfYear)) % DAILY_MESSAGES.length;
  return DAILY_MESSAGES[Math.abs(index)];
}
